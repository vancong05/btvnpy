# Bai 3: Nhap 3 so nguyen va tinh toan

a = int(input("Nhap so a: "))
b = int(input("Nhap so b: "))
c = int(input("Nhap so c: "))

# Tong va tich
print("Tong =", a + b + c)
print("Tich =", a * b * c)

# Hieu 2 so bat ky (vi du a - b)
print("Hieu a - b =", a - b)

# Chia
print("a // b =", a // b)
print("a % b =", a % b)
print("a / b =", a / b)